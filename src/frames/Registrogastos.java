    private void btnmodificargastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificargastoActionPerformed
        if (txtrazon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene los campos vacíos");
        } else {
            Gasto g = new Gasto();
            g.setId(Integer.parseInt(txtidgasto.getText()));
            g.setRazon(txtrazon.getText());
            g.setDescripcion(txtadescripcion.getText());
            g.setMonto(txtmonto.getText());

            String mensaje = gbo.modificarGasto(g);

            if (mensaje.equals("Actualizado correctamente")) {
                JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
                listarGasto();
            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        }
    }//GEN-LAST:event_btnmodificargastoActionPerformed

    private void btneliminargastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminargastoActionPerformed
        int selectedRow = tbgastos.getSelectedRow();
        String idGastoStr = (String) tbgastos.getValueAt(selectedRow, 0);
        int idGasto = Integer.parseInt(idGastoStr);
        String mensaje = gbo.eliminarGasto(idGasto);
        JOptionPane.showMessageDialog(null, mensaje);
        listarGasto();
    }//GEN-LAST:event_btneliminargastoActionPerformed

