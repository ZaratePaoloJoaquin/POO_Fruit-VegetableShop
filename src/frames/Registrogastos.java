    private void btnagregargastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregargastoActionPerformed
        if (txtrazon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene los campos vacios");
        } else {
            Gasto g = new Gasto();
            LocalDate fechaActual = LocalDate.now();
            g.setFecha(fechaActual);
            g.setRazon(txtrazon.getText());
            g.setDescripcion(txtadescripcion.getText());
            g.setMonto(txtmonto.getText());
            gbo.agregarGasto(g);
        }
        JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
        listarGasto();
    }//GEN-LAST:event_btnagregargastoActionPerformed

