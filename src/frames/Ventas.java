    private void btnregistroventaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistroventaActionPerformed
        try {
            String fecha = lblfecha.getText();
            String producto = txtproducto.getText();
            double precio = parseDoubleWithComma(txtprecio.getText());
            String medida = lblmedida.getText();
            double descuento = parseDoubleWithComma(txtdescuento.getText());
            double cantidad = Double.parseDouble(txtcantidad.getText());
            double subtotal = Double.parseDouble(txtsubtotal.getText());

            // Crear objeto de Venta
            Venta v = new Venta();
            LocalDate fechaActual = LocalDate.now();
            v.setFecha(fechaActual);
            v.setProducto(producto);
            v.setPrecio(precio);
            v.setMedida(medida);
            v.setDescuento(descuento);
            v.setCantidad(cantidad);
            v.setSubtotal(subtotal);

            // Obtener conexión a la base de datos
            Connection conn = Conexion.getConnection();

            // Agregar la venta utilizando el DAO
            ventaDao ventaDAO = new ventaDao();
            String mensaje = ventaDAO.agregarVenta(conn, v);

            // Cerrar conexión
            conn.close();

            // Mostrar mensaje de éxito
            JOptionPane.showMessageDialog(null, mensaje);

            LimpiarCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese valores numéricos válidos para precio, descuento y cantidad.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al conectar a la base de datos o al insertar la venta: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnregistroventaActionPerformed

