    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int codigo = Integer.parseInt(txtcodigo.getText());
        Producto producto = pbo.buscarIdProducto(codigo);

        if (producto != null) {
            // Actualizar los campos de texto con la información del producto
            txtproducto.setText(producto.getDescripcion());
            txtprecio.setText(String.valueOf(producto.getPrecio()));
            lblmedida.setText(producto.getMedida());
            txtdescuento.setText(String.valueOf(producto.getDescuento()));
        } else {
            JOptionPane.showMessageDialog(null, "Producto no encontrado");
            LimpiarCampos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtcantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcantidadActionPerformed
        calcularSubtotal();
    }//GEN-LAST:event_txtcantidadActionPerformed

    private void LimpiarCampos() {
        txtcodigo.setText("");
        txtproducto.setText("");
        txtprecio.setText("");
        lblmedida.setText("");
        txtdescuento.setText("");
        txtcantidad.setText("");
        txtsubtotal.setText("");
    }

    private void calcularSubtotal() {
        try {
            double precio = parseDoubleWithComma(txtprecio.getText());
            double descuento = parseDoubleWithComma(txtdescuento.getText());
            double cantidad = Double.parseDouble(txtcantidad.getText());

            double subtotal = (precio - descuento) * cantidad;

            // Formatear el subtotal con dos decimales y sin comas
            String subtotalFormateado = String.format("%.2f", subtotal).replace(',', '.');

            txtsubtotal.setText(subtotalFormateado);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese valores numéricos válidos.");
            txtcantidad.setText("");
        }
    }

    private double parseDoubleWithComma(String text) {
        // Se reemplazaran las , por .
        return Double.parseDouble(text.replace(',', '.'));
    }

