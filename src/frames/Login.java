    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        String contra = txtcontra.getText();
        String rol = cbrol.getSelectedItem().toString();

        if (contra.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese su contrase√±a");
            return;
        }
        Connection con = null;
        try {
            con = Conexion.getConnection();
            if (con != null) {
                VerificacionUsuario vu = new VerificacionUsuario();
                boolean ingreso = vu.verificacionUsuario(con, rol, contra);
                if (ingreso) {
                JOptionPane.showMessageDialog(null, "Ingreso exitoso");
                
                // Determinar qu√© frame abrir dependiendo del rol
                if (rol.equals("Trabajador")) {
                    Ventas ventasFrame = new Ventas();
                    ventasFrame.setVisible(true);
                    dispose();
                } else if (rol.equals("Administrador")) {
                    PrincipalAdmin principalAdmin = new PrincipalAdmin();
                    principalAdmin.setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Rol no reconocido");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Rol o contrase√±a incorrecta, vuelva a intentarlo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo establecer conexi√≥n a la base de datos.");
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al conectar a la base de datos: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (con != null) {
                    con.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btningresarActionPerformed

