    private void tbproductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbproductosMouseClicked
        int seleccion = tbproductos.rowAtPoint(evt.getPoint());
        txtidp.setText(tbproductos.getValueAt(seleccion, 0) + "");
        cbcategoria.setSelectedItem(tbproductos.getValueAt(seleccion, 1) + "");
        txtdescripcion.setText(tbproductos.getValueAt(seleccion, 2) + "");
        txtprecio.setText(tbproductos.getValueAt(seleccion, 3) + "");
        txtdescuento.setText(tbproductos.getValueAt(seleccion, 4) + "");
        String medida = tbproductos.getValueAt(seleccion, 5) + "";
        // Limpiar selecci√≥n de radio botones antes de establecer uno nuevo
        rbUnidad.setSelected(false);
        rbGramos.setSelected(false);
        rbKilo.setSelected(false);

        if (medida.equals("Unidad")) {
            rbUnidad.setSelected(true);
        } else if (medida.equals("G")) {
            rbGramos.setSelected(true);
        } else if (medida.equals("Kg")) {
            rbKilo.setSelected(true);
        }
        txtstock.setText(tbproductos.getValueAt(seleccion, 6) + "");
    }//GEN-LAST:event_tbproductosMouseClicked

    private void limpiarProductos() {
        txtidp.setText("");
        txtdescripcion.setText("");
        cbcategoria.setSelectedIndex(0);
        txtprecio.setText("");
        txtdescuento.setText("");
        rbUnidad.setSelected(false);
        rbKilo.setSelected(false);
        rbGramos.setSelected(false);
        txtstock.setText("");
        txtbuscarproducto.setText("");

        txtidp.requestFocus();
    }

    private void buscarProductos() {
        String dato = txtbuscarproducto.getText().trim();
        DefaultTableModel tabla = (DefaultTableModel) tbproductos.getModel();
        pbo.buscarProducto(dato, tabla);
    }

