    private void btnagregarproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarproductoActionPerformed
        if (txtidp.getText().isEmpty() || txtdescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        } else {
            Producto produc = new Producto();
            produc.setId(Integer.parseInt(txtidp.getText()));
            produc.setCategoria(cbcategoria.getSelectedItem().toString());
            produc.setDescripcion(txtdescripcion.getText());
            // Establecer precio y descuento predeterminados
            produc.setPrecio(Double.parseDouble(txtprecio.getText().isEmpty() ? "0.0" : txtprecio.getText()));
            produc.setDescuento(Double.parseDouble(txtdescuento.getText().isEmpty() ? "0.0" : txtdescuento.getText()));
            if (rbUnidad.isSelected()) {
                produc.setMedida("Unidad");
            } else if (rbGramos.isSelected()) {
                produc.setMedida("G");
            } else if (rbKilo.isSelected()) {
                produc.setMedida("Kg");
            }
            produc.setStock(Double.parseDouble(txtstock.getText()));

            String mensaje = pbo.agregarProducto(produc);
        }
        JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
        listarProducto();
    }//GEN-LAST:event_btnagregarproductoActionPerformed

    private void btnmodificarproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarproductoActionPerformed
        try {
            if (txtidp.getText().isEmpty() || txtdescripcion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Llene todos los campos");
                return;
            }
            int id = Integer.parseInt(txtidp.getText());
            String categoria = cbcategoria.getSelectedItem().toString();
            String descripcion = txtdescripcion.getText();
            // Establecer precio y descuento predeterminados
            double precio = Double.parseDouble(txtprecio.getText().isEmpty() ? "0.0" : txtprecio.getText());
            double descuento = Double.parseDouble(txtdescuento.getText().isEmpty() ? "0.0" : txtdescuento.getText());
            String medida = "";
            if (rbUnidad.isSelected()) {
                medida = "Unidad";
            } else if (rbGramos.isSelected()) {
                medida = "G";
            } else if (rbKilo.isSelected()) {
                medida = "Kg";
            }
            double stock = Double.parseDouble(txtstock.getText());

            Producto produc = new Producto();
            produc.setId(id);
            produc.setCategoria(categoria);
            produc.setDescripcion(descripcion);
            produc.setPrecio(precio);
            produc.setDescuento(descuento);
            produc.setMedida(medida);
            produc.setStock(stock);

            String mensaje = pbo.modificarProducto(produc);

            if (mensaje.startsWith("Actualizado correctamente")) {
                JOptionPane.showMessageDialog(null, "Datos modificados correctamente");
                listarProducto();
            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error en formato de n√∫mero: " + e.getMessage());
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Error de referencia nula: " + e.getMessage());
        }
    }//GEN-LAST:event_btnmodificarproductoActionPerformed

